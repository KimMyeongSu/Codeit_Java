public class Variables {
    public static void main(String[] args) {

        boolean myBoolean = true;
        // 불리안 자료형은 true / false 두가지의 값을 넣을수 있음 .

        int MyInt = 123;
        long MyLong = 12934790509L;  //int 형의 범위를 벗어나는경우 long 형의 자료형을 써주고 뒤에 L 을 붙혀 long 사용

        double MyDouble = 3.14; //실수형 자료형 double

        String MyString = "Kim MyeoungSu" ; // 문자열
        System.out.println(MyString);
    }

}
